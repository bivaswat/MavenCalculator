class Calculator {
    int summe(int value1, int value2) {
        return value1 + value2;
    }
    int subtraktion(int value1, int value2) {
        return value1 - value2;
    }
    int multiplication(int value1, int value2) {
        return value1 * value2;
    }
    int division(int value1, int value2) {
        return value1 / value2;
    }
}
